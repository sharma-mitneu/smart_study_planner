package com.smartstudyplanner.smart_study_planner_backend.model;

public class Course {
    private String id;
    private String title;

    // Getters and Setters
    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }
}